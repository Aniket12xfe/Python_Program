str1 = "new york"

# converting in uppercase
str2 = str1.upper()
print(str2)

# converting in lowercase
str3 = str1.lower()
print(str3)

# converting first character in uppercase
str4 = str1.capitalize()
print(str4)

# for stripping any whitespace

